Create Procedure spCreateCREDITEDSTOCKtable
As
Begin
		Create Table CREDITEDSTOCK(
		STOCK_ID VARCHAR(7) Not Null,
		CREDITOR_ID VARCHAR(10) Not Null,
		AMOUNT_TO_BE_PAID DECIMAL(8,2) NOT NULL,
		DATE_ISSUED DATE NOT NULL,
		FOREIGN KEY(STOCK_ID) REFERENCES STOCK ON UPDATE CASCADE,
		FOREIGN KEY(CREDITOR_ID) REFERENCES CREDITOR ON UPDATE CASCADE);
End